Fear RP shall be followed when the numbers against you are, at minimum, doubled. An example of this would be 2v1, 4v2, 6v3. If there is anything less than this, attempting to escape "at your own risk" is deemed OK. Fear RP with Law enforcement is excluded.

Law Enforcement must follow their own department's escalation of force. When outnumbered by law enforcement, attempting to escape is at your own risk.

Lethal force would also be considered in the numbers. Your Fear RP numbers only count if there is some sort of weapon drawn. If you have 4 people vs. 2 people and only 1 of the 4 has weapons out, this does not count as a 4v2 since only 1 is showing deadly force.

