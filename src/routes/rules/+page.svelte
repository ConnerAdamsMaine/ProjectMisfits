If the suspect/s informs you to not touch the radio/phone, to include your panic button, you should comply or die.

If the individual is in a 1v1 scenario where weapons are accessible to the victim, they may fight for their life.

An Individual must fear for their life if they are outnumbered 2:1 even if you are armed.

If there is an active shootout between groups (Gang v Gang/Gang v LEOs) Do not involve yourself or go towards the shots unless the RP Pertains to you or your organization.

As a Citizen or Criminal you should always value the life of others. Taking a life is serious and should only be carried out under reasonable RP circumstances.
SAMS& SAFR should fear for their lives as well and only render aid after the scene is over and LEOs clear the scene.
No Crime should be committed near Government Buildings, as in reality, there would be very slim chances of escaping detainment.

1.16: Not Valuing Life (NVL)
NVL is the act of responding to a dangerous scene in a way that is completely unrealistic and/or puts yourself in imminent harm.

Below is a non-exhaustive list of examples of this rule break:
Blatantly not fearing for your life.

Committing a violent act against a group that has you outnumbered and/or has already shown their intent to do harm.

Fleeing when surrounded by a group that has communicated that they will do harm, including law enforcement that have escalated to lethal force.

Jumping from a fatal height to flee.

Remaining in the area of an ongoing shoutout in such a way that puts you in danger.

Failing to comply with reasonable demands when your life's on the line
Breaking this rule will result in immediate disciplinary action.

