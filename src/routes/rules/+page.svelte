We expect good RP when beefing with other civilians within the city.

1.22: Robbing
When it comes to robbing civilians, we are not a 100K or Die server, and do not believe in robbing civilians constantly and just because you can.

There must be a legitimate reason to rob someone. A legitimate reason is NOT "I am a gangster," or "I needed money," or even "They hit my car." These are just a few examples of terrible reasons to rob someone.

Robbing a player should always have a very good roleplay reason behind it, and all parties involved should know and understand why the robbery is taking place.

When robbing civilians, you can OPMY ROB the person of what is on their person.
This means that vehicles, properties/MLOs, are OFF LIMITS.

Things that can be robbed include: cash, phones, or any illegal contraband, such as dirty money, drugs, weapons, ammo, armor.

Legal items, such as IDs, food/water, etc, should NOT be stolen.

You CANNOT make a player go to a bank and take out cash or transfer money to your account. This will result in a permanent ban.

SAMS, SAFR, and LEOs CANNOT BE ROBBED. They are NOT loot boxes.

There are a few exceptions regarding LEOs however, which include the recovery of evidence taken off your person related to an ongoing scene, their bodycams, radios, and phones.

You CANNOT steal SAMS or SAFR Vehicles nor loot them for medical supplies.

You may OPMY steal a LEO Vehicle as a last effort escape option, and IF you are DIRECTLY involved in that scene. However, you may NOT loot or steal items from the vehicle.

SWAT/ERT Vehicles are NOT allowed to be stolen!

Looting LEO Vehicles will result in disciplinary actions.

1.23: Robberies/Heists
Robbers CANNOT take a friend(s) hostage, one of the robbers cannot pose as a hostage, or have a fake hostage. The Robbers must go out and find a random person and take them hostage for these scenarios.

There is technically no limit on hostages, however you must be able to manage the amount of hostages you have.

Robbers - your scene must be set up before starting the robbery. You can not start the robbery and then add people to it or show up later with the hostage. You MUST have a backpack/or bag on your person while conducting the robbery, this "shows" in RP where you are putting the items you are stealing.

Negotiations can be conducted by radio or phone between the LEOs and robbers.

Taking hostages with you from a scene is NOT ALLOWED. Know that LEOs will take action to prevent this from happening.

Robbers can ask for demands during negotiations (LEOs not pursuing and taking the hostage from the scene is not an option).

1.24: Hostage-Taking
Taking a civilian hostage is only for quality roleplay.
You must NOT keep a hostage for more than ONE scene.

Hostages CAN be exchanged for people in LEOs custody prior to them being sentenced.

If a ransom is being requested in exchange for the hostage, the hostage MUST be exchanged for the money.

Do NOT take a hostage to LEOs or elsewhere, and demand that your friend be released from custody.

It is frowned upon to take civilians hostage who are working businesses. Those businesses need their employees there to make it run. Please try everything in your power to get hostages from other places.

There shall be NO FAKE HOSTAGES, meaning, you cannot ask a friend to be a hostage, in exchange for loot or pay. The same applies for random civilians. RP actually taking someone at gunpoint, and forcing them against their will.

If it is discovered that fake hostages are being utilized, there will be disciplinary actions.

There is no limit on how many hostages you can have for a scene, however you must realistically be able to manage the amount of hostages you have.

Civilians may only be taken hostage when there is a minimum of 4 LEOs on-duty.
LEOs may only be taken hostage when there is a minimum of 10 LEOs on-duty.

SAMS & SAFR personnel are NOT ALLOWED TO BE TAKEN HOSTAGE.

