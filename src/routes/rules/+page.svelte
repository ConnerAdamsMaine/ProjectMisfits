Downed Status: When a player is injured and downed (lying on their side), they must roleplay their injuries based on the nature of the injury received, such as gunshot wounds, tripping and falling, vehicle accidents, etc. What we mean by this, don't be downed on your side, and trash-talking, or talking as if you're not injured.

Being "Flat-Backed": When a player is injured and downed (laying on their back), they must roleplay their injuries as unconscious & critically injured on the verge of death if not transported and treated at a Hospital. Civilians should not communicate with anyone at this time, including the use of /me.

Vehicle Accidents: Vehicle accidents must be roleplayed, to an extent. This means, if you crash into a brick wall going 85 mph+, don't just hop out of the vehicle and run or spark a shootout. This would not realistically happen.
