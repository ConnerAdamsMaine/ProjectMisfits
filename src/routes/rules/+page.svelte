1.27: Cop Baiting
Cop Baiting is strictly prohibited.

Do not purposefully try to get LEO attention on you by doing things you would not do realistically with a Trooper/Deputy near you. This includes burning out in the middle of intersections, purposefully revving your motor at lights to bait a pullover/chase, and purposely committing traffic violations in the vicinity of LEOs.

No one wants RP forced on them. Let it occur naturally.

1.28: Impersonating Personnel
Citizens are prohibited from impersonating emergency personnel or whitelisted departments, this includes, but is not limited to: LEOs, SAMS, SAFR, the Department of Justice, even Staff.

Wearing any clothing items containing white listed department logos, LEOs issued clothing, law enforcement style hats, on a character not actively employed in that department is not permitted..... The only exception is when citizens are actively participating in a department ride along and a high visibility department vest is required to be worn.

Wearing a Judge robe and acting as a Judge is also not permitted.

1.29: Scene Interruptions with LEOs
Any time a scene is interrupted due to a player, whether LEO or Crim, crashing or as a result of a Tsunami, said scene must be paused, and resumed upon return.

The only exception to this rule, for example, if there is a police chase and other LEOs are involved and another disconnects, the scene does not have to be paused.

