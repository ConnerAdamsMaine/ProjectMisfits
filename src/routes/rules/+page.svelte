GTA Driving is NOT tolerated. This includes driving off of high places such as roofs, highways, mountains in any scenario, or taking a sports car up the side of a mountain. Keep it realistic.

If you wouldn't drive your own vehicle IRL in such a fashion, don't do it in-city either.

1.20: Rooftop Shooting
At this time, rooftop shooting is permitted. However, if it becomes an issue within the community, we will no longer allow it. So, it is advised that you choose appropriate moments to do this, and not abuse it, or constantly do it.

