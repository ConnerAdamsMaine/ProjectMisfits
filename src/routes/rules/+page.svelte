FRP is defined as not roleplaying realistically.

Failing to roleplay is strictly forbidden. Please treat your character as if you are in that character's skin, and only do what you would in the realistic world.

Examples of FRP are as follows, but not limited to:
Illegal activities in or around LEOs stations
Fake hostages
Using the swap seat function to prevent a player from entering the driver seat, or removing you from vehicles.
Disrupting/Injecting yourself into an ongoing scene which you were not involved in from the start
Talking while downed on your side, as if you have not just sustained a critical injury
Jumping off a building or parking garage, then running off instead of RPing out an injury
Being ejected from a vehicle or off a motorcycle, then running off instead of RPing out an injury
Being tased or beanbagged multiple times, then fighting or running off from LEOs
Using /me when flatbacked, outside of communicating extent of injuries

FRP will result in quick disciplinary actions. No questions asked.

1.13: Low-Quality Roleplay (LQRP)
LQRP is taking actions throughout scenes or situations that could be seen as harmful or unnecessary to any roleplaying scenario. Scenes with LQRP can result in a warning or further disciplinary actions if LQRP is consistently performed.

