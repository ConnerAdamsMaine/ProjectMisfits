Also known as "Green Grass," Green Zones must be crime-free. This includes instigating violence, selling drugs, robbing, etc. It is Fail RP to run away from a crime and enter a safe zone. If the RP begins outside of the safe zone and is brought into the safe zone you may continue the criminal RP, however, it must be realistic.

