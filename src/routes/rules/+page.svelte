LEO scenes may be third-partied, however, one must come up with an intelligent plan and execute it in the proper ways. DO NOT show up and shoot LEOs. Focus mainly on pickups, car switches, tackling, barricades, distractions, etc..

LEOs, SAMS, and SAFR are NOT ALLOWED to sell any job-related items or evidence, under NO CIRCUMSTANCES!

This is considered LEO, SAMS, and SAFR corruption, which in PM is a permanent ban.

